import java.util.Scanner;

public class num_2742 {
	public static void main(String[] args) {
		
		
		//기찍 N
		Scanner sc = new Scanner(System.in);
		
		// N 입력 받기
		int n = sc.nextInt();
		
		
		//N부터 1까지 한줄에 하나씩 출력
		for(int i=n; i>0; i--) {
			System.out.println(i);
		}
		
	}

}
