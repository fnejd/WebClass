import java.util.Scanner;

public class num_10998 {
	public static void main(String[] args) {
		
		//A×B
		
		//두 정수 A와 B를 입력받고 A×B를 출력
		Scanner sc = new Scanner(System.in);
		
		int a, b;
		a = sc.nextInt();
		b = sc.nextInt();
		
		System.out.println(a*b);
		
		sc.close();
		
	}

}
