import java.util.Scanner;

public class num_10869 {
	public static void main(String[] args) {
		
		
		//사칙연산
		
		//두 자연수 A와 B 입력
		Scanner sc = new Scanner(System.in);
		int a,b;
		
		a = sc.nextInt();
		b = sc.nextInt();
		
		//A+B
		System.out.println(a+b);
		
		//A-B
		System.out.println(a-b);
		
		//A*B
		System.out.println(a*b);
		
		//A/B
		System.out.println(a/b);
		
		//A%B
		System.out.println(a%b);
		
		
		
	}

}
